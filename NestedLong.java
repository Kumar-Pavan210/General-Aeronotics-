package com.example.generalaeronotics;

public class NestedLong {
    private String lat;
    private String lng;

    public String getLat() {
        return lat;
    }

    public String getLng() {
        return lng;
    }

    public NestedLong(String lat, String lng) {
        this.lat = lat;
        this.lng = lng;
    }
}
